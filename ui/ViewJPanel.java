    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblRecords.getModel();
        model.setRowCount(0);
        
        for(Car car: records.getRecords()){
            Object[] row = new Object[5];
            row[0] = car;
            row[1] = car.getCity();
            row[2] = car.getBrand();
            row[3] = car.getModel();
            row[4] = car.getSeatsNumber();
            
            model.addRow(row);
        }
    }
