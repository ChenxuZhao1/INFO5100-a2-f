    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblRecords.getSelectedRow();
        
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this,"Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblRecords.getModel();
        Car selectedCars = (Car) model.getValueAt(selectedRowIndex, 0);
        
        records.deleteRecords(selectedCars);
            
        JOptionPane.showMessageDialog(this,"Records deleted.");
        
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblRecords.getSelectedRow();
        
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this,"Please select a row to view.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblRecords.getModel();
        Car selectedCars = (Car) model.getValueAt(selectedRowIndex, 0);
        
        txtId.setText(String.valueOf(selectedCars.getId()));
        txtBrand.setText(String.valueOf(selectedCars.getBrand()));
        txtModel.setText(String.valueOf(selectedCars.getModel()));
        txtYear.setText(String.valueOf(selectedCars.getYear()));
        txtSeatsNumber.setText(String.valueOf(selectedCars.getSeatsNumber()));
        txtCert.setText(String.valueOf(selectedCars.isCertificate()));
        txtCity.setText(String.valueOf(selectedCars.getCity()));
        txtAvb.setText(String.valueOf(selectedCars.isAvailability()));

    }//GEN-LAST:event_btnViewActionPerformed


